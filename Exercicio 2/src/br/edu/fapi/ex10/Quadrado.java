package br.edu.fapi.ex10;


public class Quadrado {

	public void areaQuadrado(int l) {
		int resultadoarea= l * l;
		System.out.println("A area do quadrado eh "+ resultadoarea);
		
		
	}
	
	public void perimetro(int l) {
		int resultadoper= l*l*l*l;
		System.out.println("O perimetro do quadrado eh "+ resultadoper);
		
	}
}
